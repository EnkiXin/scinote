"""
evaluate_oracle.py — Stage-2 evaluation using ORACLE notes (per-question).

Loads oracle notes generated by `generate_oracle_notes.py` (keyed by
(video_id, question_id)) and runs the same Qwen-3B answer model with
that note attached.

Mirrors evaluate_scivideobench.py but reads from
{output_dir}/oracle_notes/<md5(vid|qid)>.json and writes to
{output_dir}/{tag.lower()}/eval_scivideobench[_chunkXofN].json.
"""

import argparse
import hashlib
import json
import os
import re
import sys
import time
from pathlib import Path
from typing import Optional

import torch
from qwen_vl_utils import process_vision_info
from transformers import AutoProcessor, Qwen2_5_VLForConditionalGeneration

sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))
from evaluate_scivideobench import (
    ANN_PATH, MC_SYSTEM, MAX_PIXELS, get_video_path, extract_frames, parse_mc,
)


def oracle_cache_path(output_dir, video_id, question_id):
    key = f"{video_id}|{question_id}"
    safe = hashlib.md5(key.encode()).hexdigest()[:16] + ".json"
    return Path(output_dir) / "oracle_notes" / (safe)


def load_oracle_note(output_dir, video_id, question_id):
    p = oracle_cache_path(output_dir, video_id, question_id)
    if not p.exists():
        return None
    try:
        return json.load(open(p)).get("note", None)
    except Exception:
        return None


def build_answer_messages(frames, item, note_text):
    opts = item["options"]
    options_text = "\n".join(f"{k}. {v}" for k, v in sorted(opts.items()))
    ctx = f"Visual notes:\n{note_text}\n\n" if note_text else ""
    valid_letters = "/".join(sorted(opts.keys()))
    user_text = (
        f"{ctx}Question: {item['question']}\n\n"
        f"Options:\n{options_text}\n\n"
        f"Answer ({valid_letters} only):"
    )
    return [
        {"role": "system", "content": MC_SYSTEM},
        {"role": "user", "content": [
            {"type": "video", "video": frames, "max_pixels": MAX_PIXELS},
            {"type": "text", "text": user_text},
        ]},
    ]


def load_annotations(limit=None, chunk_id=0, num_chunks=1):
    with open(ANN_PATH) as f:
        items = [json.loads(l) for l in f if l.strip()]
    if num_chunks > 1:
        items = [it for i, it in enumerate(items) if i % num_chunks == chunk_id]
    if limit:
        items = items[:limit]
    return items


def main():
    p = argparse.ArgumentParser()
    p.add_argument("--model", default="Qwen/Qwen2.5-VL-3B-Instruct")
    p.add_argument("--output", default="results_scivideobench")
    p.add_argument("--tag", required=True,
                   help="Output subdir name (e.g. c_oracle_72b, c_oracle_3b)")
    p.add_argument("--limit", type=int, default=None)
    p.add_argument("--resume", action="store_true")
    p.add_argument("--fps", type=float, default=1.0)
    p.add_argument("--max_frames", type=int, default=32)
    p.add_argument("--chunk_id", type=int, default=0)
    p.add_argument("--num_chunks", type=int, default=1)
    args = p.parse_args()

    items = load_annotations(args.limit, args.chunk_id, args.num_chunks)
    print(f"\nCondition: {args.tag} | n={len(items)} | model={args.model}")
    if args.num_chunks > 1:
        print(f"  chunk {args.chunk_id}/{args.num_chunks}")

    out_dir = Path(args.output) / args.tag.lower()
    out_dir.mkdir(parents=True, exist_ok=True)
    suffix = f"_chunk{args.chunk_id}of{args.num_chunks}" if args.num_chunks > 1 else ""
    out_path = out_dir / f"eval_scivideobench{suffix}.json"

    done_ids = set()
    results = []
    if args.resume and out_path.exists():
        prev = json.load(open(out_path))
        results = prev.get("results", [])
        done_ids = {(r["video_id"], r["question_id"]) for r in results}
        print(f"Resume: {len(done_ids)} done")

    print(f"Loading {args.model} ...")
    processor = AutoProcessor.from_pretrained(args.model)
    model = Qwen2_5_VLForConditionalGeneration.from_pretrained(
        args.model, dtype=torch.bfloat16, device_map="cuda")
    model.eval()

    n_valid = sum(1 for r in results if "error" not in r)
    n_correct = sum(1 for r in results if r.get("score", 0) >= 1.0)
    n_error = sum(1 for r in results if "error" in r)
    n_missing_note = 0

    for i, item in enumerate(items):
        qid = (item["video_id"], item["question_id"])
        if qid in done_ids:
            continue
        note = load_oracle_note(args.output, item["video_id"], item["question_id"])
        if note is None:
            n_missing_note += 1
            results.append({**{k: item[k] for k in ("video_id","question_id","discipline","question_type")},
                            "error": "missing oracle note"})
            continue
        t0 = time.time()
        try:
            vp = get_video_path(item["video_id"])
            frames = extract_frames(vp, fps=args.fps, max_frames=args.max_frames)
        except Exception as e:
            results.append({**{k: item[k] for k in ("video_id","question_id","discipline","question_type")},
                            "error": f"video error: {e}"})
            n_error += 1
            continue

        try:
            messages = build_answer_messages(frames, item, note)
            text = processor.apply_chat_template(messages, tokenize=False, add_generation_prompt=True)
            image_inputs, video_inputs, video_kwargs = process_vision_info(messages, return_video_kwargs=True)
            inputs = processor(text=[text], images=image_inputs, videos=video_inputs,
                                return_tensors="pt", **video_kwargs)
            inputs = {k: v.to(model.device) if hasattr(v, "to") else v for k, v in inputs.items()}
            with torch.no_grad():
                out = model.generate(**inputs, max_new_tokens=8)
            out_ids = out[0][inputs["input_ids"].shape[1]:]
            raw = processor.decode(out_ids, skip_special_tokens=True).strip()
        except Exception as e:
            results.append({**{k: item[k] for k in ("video_id","question_id","discipline","question_type")},
                            "error": f"gen error: {e}"})
            n_error += 1
            continue
        valid_keys = tuple(sorted(item["options"].keys()))
        pred = parse_mc(raw, valid_keys)
        gold = item["answer"]
        sc = 1.0 if pred.upper() == gold.upper() else 0.0
        n_valid += 1
        if sc >= 1.0: n_correct += 1
        dt = time.time() - t0
        emo = "✅" if sc >= 1.0 else "❌"
        print(f"[{n_valid}/{len(items)}] {emo} q={item['question_id']:>3} pred={pred!r} gold={gold} ({dt:.1f}s)")
        results.append({
            "video_id": item["video_id"], "question_id": item["question_id"],
            "discipline": item["discipline"], "question_type": item["question_type"],
            "pred": pred, "gold": gold, "score": sc, "raw": raw[:200],
        })
        if n_valid % 20 == 0:
            _save(out_path, args.tag, results, n_valid, n_error)
    _save(out_path, args.tag, results, n_valid, n_error)
    valid = [r for r in results if "error" not in r]
    acc = sum(r["score"] for r in valid) / max(len(valid), 1) * 100
    print(f"\n=== overall: {acc:.2f}%  (n={len(valid)}, errors={n_error}, missing_note={n_missing_note}) ===")
    print(f"saved: {out_path}")


def _save(out_path, tag, results, n_valid, n_error):
    valid = [r for r in results if "error" not in r]
    accuracy = (sum(r["score"] for r in valid) / max(len(valid), 1)) * 100
    json.dump({"condition": tag, "accuracy": round(accuracy, 2),
               "n_valid": n_valid, "n_error": n_error, "results": results},
              open(out_path, "w"), default=str)


if __name__ == "__main__":
    main()
